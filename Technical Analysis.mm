<node CREATED="1616047231508" ID="ID_1619754521" MODIFIED="1616122393216" POSITION="right" TEXT="Gaps">
<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <ol>
      <li>
        No overlap in the trading range (High-Low) between previous day (or timeframe) and today price. The difference is between bars Highs and Lows, not Opens and Closes
      </li>
      <li>
        Identify a gap at the start of the day
      </li>
      <li>
        Can not measure a gap until the day is over
      </li>
      <li>
        Upside Gap: Measure from yesterday High to today Low
      </li>
      <li>
        Downside Gap: Measure from yesterday Low to today High
      </li>
    </ol>
  </body>
</html></richcontent>
